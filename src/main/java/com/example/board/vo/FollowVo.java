package com.example.board.vo;

import lombok.AllArgsConstructor;
import lombok.NoArgsConstructor;

@NoArgsConstructor
@AllArgsConstructor
public class FollowVo {
	private Integer followIdx;
	private String memId;
	private String toMemId;
	
	
	
	public FollowVo() {
		super();
		
	}


	
	

	public FollowVo(Integer followIdx, String memId, String toMemId) {
		super();
		this.followIdx = followIdx;
		this.memId = memId;
		this.toMemId = toMemId;
	}





	public Integer getFollowIdx() {
		return followIdx;
	}



	public void setFollowIdx(Integer followIdx) {
		this.followIdx = followIdx;
	}



	public String getMemId() {
		return memId;
	}



	public void setMemId(String memId) {
		this.memId = memId;
	}



	public String getToMemId() {
		return toMemId;
	}



	public void setToMemId(String toMemId) {
		this.toMemId = toMemId;
	}
	
	

	
	
	
	
	
	
}
